#pragma once

typedef uint32_t DgResult;

// eeee llll mmmm mmmm cccc cccc cccc cccc
#define DG_MAKE_RESULT(e, l, m, c) (DgResult)((e << 28) + (l << 24) + (m << 16) + c)
#define DG_GET_RESULT_ENGINE(e)    ((e >> 28))
#define DG_GET_RESULT_LEVEL(e)     (e - ((e >> 28) << 28) >> 24)
#define DG_GET_RESULT_MODULE(e)    (e - ((e >> 24) << 24) >> 16)
#define DG_GET_RESULT_CODE(e)      (e - ((e >> 16) << 16))

#define DRAGON_AUDIO_ENGINE_TYPE        0x0
#define DRAGON_COMPUTE_ENGINE_TYPE      0x1
#define DRAGON_CORE_ENGINE_TYPE         0x2
#define DRAGON_GRAPHICS_ENGINE_TYPE     0x3
#define DRAGON_PHYSICS_ENGINE_TYPE      0x4

#define DRAGON_ERROR_BIT   0x8
#define DRAGON_WARNING_BIT 0x4
#define DRAGON_DEBUG_BIT   0x2
#define DRAGON_STATUS_BIT  0x1

#define DRAGON_SELF_ERROR_ID 0x00
#define DRAGON_GLFW_ERROR_ID 0x01
#define DRAGON_VULKAN_ERROR_ID 0x02
#define DRAGON_BOOST_ERROR_ID 0x03
#define DRAGON_VMA_ERROR_ID 0x04
#define DRAGON_OPENAL_ERROR_ID 0x05

#define DG_SUCCESS 0x00000000

#define DG_VK_CREATE_INSTANCE_FAILED                   DG_MAKE_RESULT(DRAGON_CORE_ENGINE_TYPE, DRAGON_ERROR_BIT, DRAGON_VULKAN_ERROR_ID, 0x0000)
#define DG_VK_EXTENSION_NOT_PRESENT                    DG_MAKE_RESULT(DRAGON_CORE_ENGINE_TYPE, DRAGON_ERROR_BIT, DRAGON_VULKAN_ERROR_ID, 0x0001)
#define DG_VULKAN_DEBUG_UTILS_INSTANCE_CREATION_FAILED DG_MAKE_RESULT(DRAGON_CORE_ENGINE_TYPE, DRAGON_ERROR_BIT, DRAGON_VULKAN_ERROR_ID, 0x0002)